export const translate = (key: string, language: "en" | "zh" = "en"): string => {
  // Skip translation for Pokemon names and properties
  if (key.startsWith("pokemon-") || key === "type" || key === "rarity") {
    return key;
  }
  
  return translations[language]?.[key] || key;
};

export const getCurrentLanguage = (): "en" | "zh" => {
  const savedLanguage = localStorage.getItem("language");
  return (savedLanguage === "zh" ? "zh" : "en");
};

export const changeLanguage = (language: "en" | "zh"): void => {
  localStorage.setItem("language", language);
  
  // Dispatch event to notify components
  const event = new CustomEvent("languageChange", {
    detail: { language }
  });
  
  window.dispatchEvent(event);
};
